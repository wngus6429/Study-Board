const DYNAMIC_TRANSLATIONS: Array<[RegExp, (match: RegExpMatchArray) => string]> = [
  [/^총 ([\d,]+)개 게시글$/, (match) => `投稿 ${match[1]}件`],
  [/^([\d,]+)개 글$/, (match) => `${match[1]}件の投稿`],
  [/^게시글 ([\d,]+)개$/, (match) => `投稿 ${match[1]}件`],
  [/^구독자 ([\d,.]+)명$/, (match) => `購読者 ${match[1]}人`],
  [/^생성자: (.+)$/, (match) => `作成者: ${match[1]}`],
  [/^현재 페이지 \((\d+) \/ (\d+)\)$/, (match) => `現在のページ (${match[1]} / ${match[2]})`],
  [/^현재 페이지 \((\d+)페이지\)$/, (match) => `現在のページ (${match[1]}ページ)`],
  [/^블라인드 시간: (.+)$/, (match) => `ブラインド日時: ${match[1]}`],
  [/^"(.+)" 사용자의 블라인드를 해제하시겠습니까\?$/, (match) => `"${match[1]}" ユーザーのブラインドを解除しますか？`],
  [/^(.+) 사용자의 블라인드를 해제하시겠습니까\?$/, (match) => `${match[1]} ユーザーのブラインドを解除しますか？`],
  [/^(.+)님이 만든 채널입니다\. 다양한 주제로 소통해보세요! ✨$/, (match) => `${match[1]}さんが作成したチャンネルです。さまざまなテーマで交流しましょう! ✨`],
  [/^구독자 ([\d,.]+)명 · 게시글 ([\d,]+)개$/, (match) => `購読者 ${match[1]}人 · 投稿 ${match[2]}件`],
  [/^(.+) 탭$/, (match) => `${translateKoreanText(match[1])}タブ`],
];

export function translateKoreanText(value: string): string {
  if (!/[가-힣]/.test(value)) {
    return value;
  }

  const trimmed = value.trim();
  const exact = KO_TO_JA_EXACT[trimmed];

  if (exact) {
    return value.replace(trimmed, exact);
  }

  for (const [pattern, replacer] of DYNAMIC_TRANSLATIONS) {
    const match = trimmed.match(pattern);
    if (match) {
      return value.replace(trimmed, replacer(match));
    }
  }

  return value;
}
